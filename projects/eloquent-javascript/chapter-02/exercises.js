// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
let hold = "";
for (let i = 0; i < size; i++){
  if(i % 2 === 0){
    for (let j = 0; j < size; j++){
      if(j % 2 === 0){
        hold = hold + " ";
      } else {
        hold = hold + "#";
      }
    } hold = hold + "\n"
  } else {
    for (let k = 0; k < size; k++){
      if (k % 2 === 0){
        hold = hold + "#";
      } else{
        hold = hold + " ";
      }
    } hold = hold + "\n";
  }
} console.log(hold);
}

