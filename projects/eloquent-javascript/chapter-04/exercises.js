// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {
  //given an array, create an object for each element in the array while nesting the previous object
  // starting at the last element will be easier
  let obj = {};
  for (let i = array.length - 1; i >= 0; i--){
    if(i === array.length - 1){
      obj.value = array[i];
      obj.rest = null;
    } else{
      obj = {
        value: array[i],
        rest: obj
      };
    }
  } return obj;
}

