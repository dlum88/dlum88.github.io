// need an array to hold pushed values; return values at end
//
function listToArray(list){
  let hold = [];
  if (list.value){
    hold.push(list.value);
  }
  let nextPos = list.rest;

  while (nextPos){
    hold.push(nextPos.value);
    if (nextPos.rest){
      nextPos = nextPos.rest
    } else {
      break;
    }
  } return hold;
}


